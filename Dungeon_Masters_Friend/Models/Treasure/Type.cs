namespace Dungeon_Masters_Friend.Models.Treasure
{
    /// <summary>
    /// Type of a treasure item.
    /// </summary>
    public enum Type
    {
        /// <summary>
        /// An item with a distinct use that is commonly sold or bartered
        /// </summary>
        TradeGood,
        /// <summary>
        /// A valuable mineral or gem
        /// </summary>
        Gemstone,
        /// <summary>
        /// An item of artistic, historical, or religious significance
        /// </summary>
        ArtObject
    }
}
